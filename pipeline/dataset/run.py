"""Phase 1 orchestrator: build_text → embed → cluster → discovery_report.

Run:  python -m pipeline discover [--model <st-model>]
Stops after the discovery report; the taxonomy proposal (Phase 2) is generated by
`taxonomy.py` once the report is reviewed.
"""

from __future__ import annotations

import logging

from .cluster import run_cluster
from .discover import run_discover
from .embed import DEFAULT_MODEL, run_embed
from .taxonomy import run_taxonomy
from .text import run_build_text

log = logging.getLogger("pipeline.dataset.run")


def run_discovery_pipeline(model_name: str = DEFAULT_MODEL) -> dict:
    print(f"\n{'='*64}\nDATA DISCOVERY — model={model_name}\n{'='*64}")
    text_df = run_build_text()
    print(f"  jobs_text: {len(text_df)} rows "
          f"(lang vi={int((text_df['lang']=='vi').sum())} en={int((text_df['lang']=='en').sum())})")
    emb = run_embed(model_name, text_df)
    print(f"  embeddings: {len(emb)} x {emb['dim'].iloc[0]}")
    clusters, meta = run_cluster(emb, text_df)
    print(f"  clusters: best k(all)={meta['best_k_all']} sil={meta['silhouette_all']}")
    if meta.get("best_k_other"):
        print(f"            OTHER best k={meta['best_k_other']} sil={meta['silhouette_other']}")
    run_discover(text_df, clusters, meta)
    run_taxonomy(text_df, clusters, meta)
    print(f"\nDone. Report: data/dataset/discovery/discovery_report.md")
    print("       Taxonomy proposal: ref/taxonomy/taxonomy_v1.yml")
    print("DECISION GATE: review report + taxonomy proposal before annotation (Phase 3+).\n")
    return {"text_df": text_df, "emb": emb, "clusters": clusters, "meta": meta}
